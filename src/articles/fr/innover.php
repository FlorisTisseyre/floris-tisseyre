		Comment innover ?
	</div>
	<div class="content">
		<div class="description">
			<div class="ui small header">« Qu’est-ce que tu peux nous dire sur l’innovation ? »</div>
			<p>Dès qu’on évoque l’innovation, on pense à ces sociétés qui ont inventé une nouvelle technologie ou fabriqué un produit révolutionnaire.</p>
			<p>Je pense pourtant que l’innovation est plus vaste, dans tout ce qui change radicalement notre manière de faire, que ce soit notre manière d’organiser, de manager, de coder, …</p>
			
			<div class="ui small header">« C’est-à-dire ? Tu peux nous illustrer par une de tes expériences ? »</div>
			<p>Oui bien sûr, je pense que les journées « Coup d’envoi » quand j’étais Change Leader à Arcelor Mittal illustreront bien mon propos</p>
			
			<div class="ui small header">« Et toi concrètement, tu as fait quoi ? »</div>
			<p>Quelques mois après mon arrivée dans le service, on m’a demandé d’organiser le lancement du nouveau plan d’entreprise : Med2020. Ayant moins de deux mois pour l’organiser, on s’attendait à ce que je fasse comme par le passé : une présentation en grande pompe pour chaque secteur de l’usine, soit 6 conférences d’environ 500 participants.</p>
			
			<div class="ui small header">« Ça semble raisonnable vu les délais »</div>
			<p>Effectivement, mais j’aurais obtenu la même réaction que les fois précédentes : « encore un nouveau plan qui ne va rien changer au final ».</p>
			<p>J’ai fait le pari de faire comprendre à chacun des 3000 salariés ce que pouvait leur apporter la WCM. Pour la culture, c’est une approche englobant la TPM de Toyota, le Lean, le Six Sigma et la théorie des contraintes de Goldratt.</p>
			<p>Les ouvriers, comme la plupart des gens d’ailleurs, ne comprennent vraiment que s’ils manipulent de leur mains. Et pour faire des ateliers pratiques, les groupes ne pouvaient pas dépasser 15 personnes.</p>
			
			<div class="ui small header">« Waouh ! Mais ça fait des centaines de séances ? »</div>
			<p>Oui à peu près 240 et ce n’était pas le plus dur.</p>
			<p>Pour ce que je voulais faire, il fallait un espace dédié, pas les amphis que nous utilisions d’ordinaire. Il a donc fallu transformer un entrepôt de 500 m² pour aménager des zones de projection et des zones de workshop où les groupes pourraient faire des expériences liées à la WCM.</p>
			<p>Mais surtout, pour faire comprendre ce que la WCM leur apporterait, il a fallu donner du sens à cette méthode, en trouver la valeur et concevoir des ateliers qui mettaient en évidence les bénéfices.</p>
			<p>Et enfin, nous avons formé les 30 personnes qui sont devenus les leaders de la WCM et qui ont animés les 240 ateliers.</p>
			
			<div class="ui small header">« Tu considères cette expérience comme une innovation, n’est-ce pas ? »</div>
			<p>Parfaitement ! Nous avons changé radicalement la manière de lancer un plan d’entreprise et ça a donné des résultats spectaculaires :</p>
			<ul class="noBulletList">
				<li>La très grande majorité des salariés était satisfaite et prête à être acteur de cette transformation et, plus rare encore, l’ensemble des syndicats a « applaudi » la démarche.</li>
			</ul>
			<br/>
			<p>Et j’ai depuis continué cette même démarche d’innovation à Anaqua Services.</p>
