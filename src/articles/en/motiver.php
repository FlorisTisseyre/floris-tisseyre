	<div class="header">
		How to motivate?
	</div>
	<div class="content">
		<div class="description">
			<div class="ui small header">« Where does motivation comes from? »</div>
			<p>From wages? Those who talk about it the most are usually the least motivated.</p>
			<p>Today, I bet it all on « learning »: discover and help discover new things constantly. It is daring and far from theories but it works, at least with my current team and the previous one.</p>
			
			<div class="ui small header">« Constantly? How? »</div>
			<p>During daily Scrum meeting, sprint retrospectives, in the form of workshops, someone submit a question to the team and we try to answer it. Here is a (very) small excerpt of questions of the Anaqua Services IT team:</p>
			<ul class="noBulletList">
				<li>« How to track users requests? »</li>
				<li>« Users are not that happy with IT, what are we doing wrongly, ho can we improve? »</li>
				<li>« Physical servers, why not virtualized? Cloud ? »</li>
				<li>« How do we boost meetings? »</li>
				<li>« Steph has difficulties working from home. Can we help? »</li>
				<li>« Unit testing yes, but where do we begin? »</li>
				<li>…</li>
			</ul>
			<p>There is so much that at the end, the hard part is to prioritie them, not to find them.</p>
			
			<div class="ui small header">« And when motivation decline? »</div>
			<p>I've found a very simple way: the question « what's up? ». If he or she answers « as usual », motivation is probably declining or it will soon.</p>
			<p>Thus, I seek with him in one-on-one how to reboost him: it could be a new challenge of simply see things differently.</p>
			
			<div class="ui small header">« And if there is nothing left to learn? »</div>
			<p>Computing (and management) is so vast and in rapid development that believing that you have covered it all is a mistake and shows that it's time to carry out a self-examination.</p>
			
			<div class="ui small header">« No teammate willing to resign? »</div>
			<p>Yes and as no one can be held against his will, if he wants to leave and discover new things elsewhere, it is manager duty to support him in this change.</p>
			<p>It has happened several times. I did my best every time to understand how I could help. It has never accomplished because he or she has found new challenges to address.</p>
