		Evolve, or how to stay flexible?
	</div>
	<div class="content">
		<div class="description">
			<div class="ui small header">« What is required to evolve? »</div>
			<p>To face changes, you first need a team of people that are motivated, united and capable of innovation but you need more than that.</p>
			<p>Let it be at Arcelor Mittal or at Anaqua Services, I found the same scenario: the software grows and becomes more and more complex. In a few years period, maintaining it is nearly impossible, it meets less and less the needs and it slows down the company growth.</p>
			<p>Faced with this tragedy, executive managers decide that a recast is necessary, except that it takes years and during that time, great challenges of the company are postponed.</p>
			<p>And for what? A few years after this colossal project, the new software has become obsolete too. At Arcelor Mittal, I have even seen the absurd situation where the new software became a truly labyrinthine system even before the end of the project.</p>
			
			<div class="ui small header">« And can't we help? »</div>
			<p>Yes but it requires many things ; here is our adventure in Anaqua Services:</p>
			<p>It all start from the Agile Pays Basque seminar where we have discovered new opportunities that we couldn't waste.</p>
			<p>Two weeks later, Claude Aubry came for coaching on his visual management and his short cycles. Since then, we deliver every 15 days, where projects took months, even years.</p>
			<p>But this brought another problem: to do iterations, you have to rewrite code … and it's hard … and it takes ages … and so, we don't feel like doing it.</p>
			<p>We today follow the Clean Code path: « Every beat of code should reveal its intent ». To achieve this ideal, we have to constantly refactor the code ; the unit tests being there to insure that we didn't break everything at the same time.</p>
			
			<div class="ui small header">« And if we have to deliver and that we don't have time to clean? »</div>
			<p>No worries: we do our best but more than anything, we keep in mind that we have produced a code hardly maintanable and scalable. We have juste created a « technical debt » that will need to be paid the day where the company will need to evolve. The more we wait to reimburse this debt, the more it will cost to the company.</p>
			
			<div class="ui small header">« Everything is there? Some Scrum and some Clean Code? »</div>
			<p>No, and far from that. We are just at the beginning of the path and we appeal to a craftman, Arnaud Lemaire, to guide us during this adventure.</p>
			<p>Today we explore Domain Driven Design, or how to put business (rules, knownledge, intelligence, ...) at the very core of the software, how to reconcile IT and business. In this approach, technique, namely architecture, design but also programming language and bdd storage, have to be pushed very far away in debates and it has to be easily replaced.</p>
			
			<div class="ui small header">« A last word? »</div>
			<p>« Evolving » imply to stay flexible over time. This approach allow that. Surely, long is the way, but the opportunity is unique.</p>
