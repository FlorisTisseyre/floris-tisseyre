		Comment motiver ?
	</div>
	<div class="content">
		<div class="description">
			<div class="ui small header">« D’où vient la motivation ? »</div>
			<p>Du salaire ? ceux qui en parlent le plus sont bien souvent les moins motivés.</p>
			<p>Aujourd’hui je mise tout sur « apprendre » : découvrir et faire découvrir de nouvelles choses en permanence. C’est osé et loin des théories mais ça marche, en tout cas avec mon équipe actuelle et la précédente.</p>
			
			<div class="ui small header">« En permanence, comment ? »</div>
			<p>Lors des mêlées du matin, des rétrospectives de sprints, sous forme d’ateliers, quelqu’un soumet une question à l’équipe et on essaye d’y répondre. Voici un (tout) petit échantillon des interrogations de l’équipe IT d’Anaqua Services :</p>
			<ul class="questions">
				<li>« Comment suivre les demandes ? »</li>
				<li>« Les utilisateurs ne sont pas super contents de l’IT, qu’est-ce qui cloche ? »</li>
				<li>« Serveurs physiques, pourquoi pas virtuels ? Cloud ? »</li>
				<li>« Comment on booste les réunions ? »</li>
				<li>« Steph a du mal à travailler avec nous de chez lui. On peut l’aider ? »</li>
				<li>« Des tests unitaires oui, mais on commence par où ? »</li>
				<li>…</li>
			</ul>
			<p>Il y en a tellement qu’au final, la difficulté est de les prioriser, pas de les trouver.</p>
			
			<div class="ui small header">« Et quand la motivation retombe ? »</div>
			<p>J’ai trouvé un moyen très simple : la question « quoi de neuf ? ». Si il ou elle me répond « la routine quoi », la motivation est surement en baisse ou ça ne va pas tarder.</p>
			<p>Du coup, je cherche avec lui en one-on-one comment le re-booster : ça peut être un nouveau challenge ou tout simplement voir les choses sous un autre angle.</p>
			
			<div class="ui small header">« Et s’il n’y a plus rien à apprendre ? »</div>
			<p>L’informatique (et le management) est tellement vaste et en évolution rapide que croire qu’on en a fait le tour est une erreur et montre qu’il est temps de se remettre en question.</p>
			
			<div class="ui small header">« Jamais de collaborateur qui voulait partir ? »</div>
			<p>Si et comme on ne retient pas quelqu’un contre sa volonté, s’il veut partir et découvrir de nouvelles choses ailleurs, le manager se doit de l’accompagner dans ce changement.</p>
			<p>La situation s’est présentée à plusieurs reprises. J’ai fait de mon mieux à chaque fois pour comprendre comment je pouvais l’aider. Ça ne s’est jamais concrétisé car il ou elle a su trouver de nouveaux défis à relever.</p>
