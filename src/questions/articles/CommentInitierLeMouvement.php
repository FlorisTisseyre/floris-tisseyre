<p>
	La moteur de tout changement, c'est l'<b>urgence</b> ! Pas d'urgence, pas de changement !
</p>
<p>
	Notre instinct nous dicte de rester dans ce que nous connaissons car c'est là que se trouve la  sécurité.<br />
	Vu que changement est synonyme d'inconnu, pourquoi alors voudrions-nous changer ?<br />
	Et bien, aussi étonnant que ça puisse paraître, la réponse se trouve aussi dans le danger.<br />
</p>
<p>
	Faites réaliser à une équipe qu'une situation <b>ne peut pas</b> durer, que si on continue comme ça, tout va s'éffronder, qu'il <u>faut</u> changer : là, maintenant, tout de suite !!<br />
	Il se passe à ce moment quelque chose de formidable : le changement devient une question de survie.<br />
</p>
<p>
	Félicitation, vous avez <b>initier le mouvement</b>.<br />
</p>
<p>
	Et le manager à rapidement la grande joie de se retrouver devant un nouveau problème existentiel : <b>Comment maintenir le mouvement ?</b>
</p>