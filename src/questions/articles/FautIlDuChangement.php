<p>
	J'adore cette question. Non pas parce qu'il est difficile d'y répondre, mais parce que c'est elle qui anime toute personne qui s'oppose au changement.
</p>
<p>
	Pas difficile ? C'est-à-dire ?
</p>
<p>
	Notre société change. Elle se rationalise, s'industrialise, s'automatisée de plus en plus.<br/>
	L'argent devient le centre de tout, le nerf de la guerre.<br/>
	Le marché devient de plus en plus compétitif et il faut suivre si on ne veut pas se faire avaler par le système.<br/>
	Ce constat est d'ailleurs vrai au niveau des entreprises, mais aussi au niveau des équipes et en tant qu'individu.<br/>
	Dans ce monde, ne pas changer, c'est la mort assurée.
</p>
<p>
	Alors, il y a bien sûr une autre vision de tout ça, une pensée qui rejette l'argent comme fin en soi et qui dit que nous devrions aspirer à plus.<br/>
	Pas de changement donc pour ces gens et ces sociétés ?<br/>
	C'est bien tout l'inverse qui se produit : pour aspirer à plus il faut engager des transformations encore plus profonde dans les entreprises.
</p>
<p>
	Mais alors, il reste une question :<br/>
	<b>Comment se fait-il qu'on entende si souvent "je fais comme ça depuis des années et ça a toujours bien marché" ?</b><br/>
	De manière assez étonnante, ce sont bien souvent ces mêmes personnes qui critiquent le manque de changement au sein de l'entreprise.
</p>
<p>
	Sur ce point, Albert Einstein nous offre une fois de plus un point de vue mordant sur la question :
<div class="ui teal message">La folie, c'est se comporter de la même manière et s'attendre à un résultat différent.</div>
</p>
<p>
	En réalité, la question à se poser n'est pas "Faut-il du changement ?" : il est nécessaire et il est bien vain de lutter contre.<br/>
	Non, la vraie question est : <b>"Faut-il tout changer ?"</b>
</p>